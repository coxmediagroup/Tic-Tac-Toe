module TicTacToe

  #
  #  Define infinity. Please note that Ruby represents the result of a division by
  #  zero (e.g., 1.0/0) as +/- Infinity. Comparison operations on this value behave
  #  as one might intuit (i.e., -Infinity < [real n] < +Infinity.)
  #
  INFINITY = 1.0/0

end